// Method: ColorTableAttributes::ColorTableAttributes
//
// Purpose:
//   Copy utility for the ColorTableAttributes class.
//
// Note:       Autogenerated by xml2atts.
//
// Programmer: xml2atts
// Creation:   omitted
//
// Modifications:
